package ro.katy.tutorial;

public class Double {
	
	public static void main(String[] args) { 
		
			/*The double data type is a double-precision 64-bit IEEE 754 floating-point. For decimal values, 
			this data type is generally the default choice.
		
		Syntax:
		
		double doubleVar;
		Size: 8 bytes or 64 bits
		
		Values: Upto 16 decimal digits
		
		Note: 
		
		The default value is taken as ‘0.0’.
		Both float and double data types were designed especially for scientific calculations, where approximation errors are acceptable. 
		If accuracy is the most prior concern then, it is recommended not to use these data types and use BigDecimal class instead. */
		
		
		double no = 4.65475587d; 
		
		System.out.println(no); // 4.65475587
		

		
		
			}

}
