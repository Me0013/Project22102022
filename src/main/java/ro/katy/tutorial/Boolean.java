package ro.katy.tutorial;

public class Boolean {
	
	public static void main(String[] args) {
		
		int a = 10, b = 30; 
		
		boolean check1 = a == b; 
		
		if (check1 == true) {
			System.out.println(" a is equal to b.");
		} else if (a > b) {
			System.out.println(" a is bigger then b."); 
		} else {
			System.out.println("a is smaller then b.");
			
		}
	
	}

}
